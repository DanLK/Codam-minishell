#include "lexer.h"

bool	issymbol(char c)
{
	return (c == '_' || (c >= '!' && c <= '/')
		|| c == ';' || c == '?' || c == '@'
		|| c == '[' || c == ']' || c == '{'
		|| c == '}' || c == '~' || c == '^'
		|| c == '=');
}

bool	is_special_char(char c)
{
	return (c == ' ' || c == '|' || c == '&'
		|| c == '<' || c == '>' || c == '\"' || c == '\'');
}

bool	is_token_breaker(char c)
{
	return (c == ' ' || c == '\t' || c == '|' || c == '<' || c == '>');
}
